package algorithms.linkedlist;

/**
 * @className: ListNode
 * @description: 链表基础数据结构
 * @author: Miluo
 * @date: 2023/2/8
 **/
public class ListNode {
    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) { this.val = val; }
    ListNode(int val, ListNode next) { this.val = val; this.next = next; }


}
